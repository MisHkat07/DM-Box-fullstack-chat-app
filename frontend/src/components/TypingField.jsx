const TypingField = ({
  inputHandle,
  newMessage,
  sendMessage,
  emojiHandler,
  imageHandle,
  emojiShow,
  emojiShowHandler,
}) => {
  const [sender, setSender] = useState(false);

  const handleChange = () => {
    setSender(true);
  };
  return (
    <div className="type-msg-section">
      <div className="file hover-attachment">
        <div className="add-attachment">Add attachment</div>
        <BsPlusCircle />
      </div>
      <div className="file hover-image">
        <div className="add-image">Add Image</div>
        <input
          onChange={imageHandle}
          type="file"
          className="form-control"
          id="pic"
        />
        <label htmlFor="pic">
          <RiGalleryLine />
        </label>
      </div>
      <div className="file">
        <BiMessageAltEdit />
      </div>
      <div className="file hover-gift">
        <div className="add-gift">Add Gift</div>
        <AiFillGift />
      </div>
      <div className="msg-type">
        <textarea
          onInput={!sender ? handleChange : ""}
          onChange={(e) => inputHandle(e)}
          value={newMessage}
          onFocus={emojiShow ? emojiShowHandler : ""}
          type="text"
          className="form-control msg-input"
          name="message"
          id="message"
          onKeyPress="this.style.width = ((this.value.length + 1) * 8) + 'px';"
          placeholder="Aa"
        />
        <label onClick={emojiShowHandler} htmlFor="emoji" className="emo">
          😃
        </label>
      </div>
      <div onClick={sendMessage} className="file" id="main-icon">
        {sender && newMessage ? (
          <label className="send">
            <MdSend />
          </label>
        ) : (
          <label className="wave">
            <MdWavingHand />
          </label>
        )}
      </div>
      {emojiShow ? (
        <div className="emoji-section">
          <div className="emojis">
            {emojis.map((emoji, i) => (
              <span key={i} onClick={() => emojiHandler(emoji)}>
                {emoji}
              </span>
            ))}
          </div>
        </div>
      ) : (
        ""
      )}
    </div>
  );
};

export default TypingField;
